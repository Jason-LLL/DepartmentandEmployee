    /**
     * 检查SD卡是否存在
     */
    public static boolean checkSDcard() {
        return Environment.MEDIA_MOUNTED.equals(Environment
                .getExternalStorageState())
                || !Environment.isExternalStorageRemovable();
    }

    /**
     * 默认返回(/mnt/sdcard/android/data/包名/cache) ，不存在此目录返回根目录下的cache目录
     *
     * @param context
     * @return
     */
    public static String getAppCachePath(Context context) {
        String path = null;
        if (context != null && checkSDcard()) {
            if (context.getExternalCacheDir() != null) {
                path = context.getExternalCacheDir().getAbsolutePath();
            } else {
                path = context.getCacheDir().getPath();
            }
        }
        return path;
    }

    /**
     * 默认返回(/mnt/sdcard/android/data/包名/cache) ，不存在此目录返回根目录下的cache目录
     *
     * @param context
     * @return
     */
    public static String getAlbumPath(Context context) {
        String path = null;
        if (context != null && checkSDcard()) {
            if (Environment.getExternalStorageDirectory() != null) {
                path = Environment.getExternalStorageDirectory() + "/hewoxue";
                File file = new File(path);
                file.mkdirs();
            }
        }
        return path;
    }

    /**
     * * 清除外部cache下的内容(/mnt/sdcard/android/data/com.xxx.xxx/cache)
     */
    public static void cleanAppCache(Context context) {
        if (Environment.getExternalStorageState().equals(Environment.MEDIA_MOUNTED)) {
            deleteFolderFile(getAppCachePath(context), false);
        }
    }

    /**
     * 删除指定目录下文件及目录
     *
     * @param deleteThisPath
     * @param filePath
     */
    public static void deleteFolderFile(String filePath, boolean deleteThisPath) {
        if (!TextUtils.isEmpty(filePath)) {
            try {
                File file = new File(filePath);
                if (file.isDirectory()) {
                    File files[] = file.listFiles();
                    for (File delFile : files) {
                        deleteFolderFile(delFile.getAbsolutePath(), true);
                    }
                }
                if (deleteThisPath) {
                    if (!file.isDirectory()) {
                        file.delete();
                    } else {
                        if (file.listFiles().length == 0) {
                            file.delete();
                        }
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

